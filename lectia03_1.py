# Bucla repetitiva // loop

# for   ->  cunosc numarul de iteratii/repetari
# while ->  nu cunosc numarul de executii
print("Exercitiu for: ")
for numar in range(1,10):
    print(numar)

print("\nExercitiu while: ")
nr = 1
while nr <= 9:
    print(nr)
    nr += 1

print("\nExercitiu for cu pas: ")
# numerele pare <= 10
for numar in range(0, 12, 2):
    print(numar)

